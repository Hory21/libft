char	*ft_strchr(char *s, int c)
{
	char	ch;
	int		i;

	ch = (char)c;
	i = 0;
	while (s[i] != ch && s[i] != 0)
	{
		if (s[i] == ch)
			return (s + i);
		i++;
	}
	if (s[i] == ch)
		return (s + i);
	return (0);
}
