char	*ft_strrchr(char *s, int c)
{
	char	ch;
	int		i;
	char	*ret;

	ret = 0;
	ch = (char)c;
	i = 0;
	while (s[i] != 0)
	{
		if (s[i] == ch)
			ret = s + i;
		i++;
	}
	if (s[i] == ch)
		ret = s + i;
	return (ret);
}
